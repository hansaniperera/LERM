    private void updateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBActionPerformed
        // TODO add your handling code here:
            
            userCombo2.removeAllItems();
            //unicCombo.removeAllItems();
            userCombo2.addItem(UserType.ADMIN.name());
            userCombo2.addItem(UserType.OFFICER.name());
        if((apwdF1.getText().equals(acpwdF1.getText()))){
             try {
            user.updateUser(lnameT2.getText(), fnameT2.getText(), atelT1.getText(), aaddT1.getText(),Integer.parseInt(String.valueOf(unicCombo.getSelectedItem())),aemailT1.getText());
            
               
                                   
                    if(userCombo2.getSelectedItem()=="OFFICER"){
                        int loadId = off.loadId();
                        off.updateUser(loadId,ausernameT.getText(),apwdF.getText(),Integer.parseInt(String.valueOf(unicCombo.getSelectedItem())));
                    }else{
                        
                        int loadId1 = ad.loadId();
                        ad.updateUser(loadId1,ausernameT.getText(),apwdF.getText(),Integer.parseInt(String.valueOf(unicCombo.getSelectedItem())));
                    }
                    
                }
                catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "username already exist \n please change your user name");
                }    catch (SQLException ex) {
                         Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
                     }
            }
            else{
                JOptionPane.showMessageDialog(this, "Password fields do not match");
            }
       
    }//GEN-LAST:event_updateBActionPerformed

