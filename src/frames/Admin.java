    private void jDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteBtnActionPerformed

            int row = jUsersTbl.getSelectedRow();
            int nic =(int) jUsersTbl.getValueAt(row,0);
        
            try {
            pst1 = Db.connection().prepareStatement("delete from admin where nic =?");
            pst1.setInt(1, nic);
            pst1.executeUpdate();
            pst1 = Db.connection().prepareStatement("delete from person where nic =?");
            pst1.setInt(1, nic);
            pst1.executeUpdate();
            System.out.println("DELETED SUCCESSFULLY");
            
            if(row != -1) {
            DefaultTableModel model = (DefaultTableModel)jUsersTbl.getModel();
            model.removeRow(row);
            }
            
            } catch (SQLIntegrityConstraintViolationException ex) {
                try {
                    pst1 = Db.connection().prepareStatement("delete from officer where nic =?");
                    pst1.setInt(1, nic);
                    pst1.executeUpdate();
                    pst1 = Db.connection().prepareStatement("delete from person where nic =?");
                    pst1.setInt(1, nic);
                    pst1.executeUpdate();
                } catch (SQLException ex1) {
                    Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
            catch (SQLException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
                    
    }//GEN-LAST:event_jDeleteBtnActionPerformed

